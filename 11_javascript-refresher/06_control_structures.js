// If...else if...else for grades
let score = 85;

if (score >= 90) {
  console.log("A");
} else if (score >= 80) {
  console.log("B");
} else if (score >= 70) {
  console.log("C");
} else {
  console.log("F");
}

// For loop
for (let i = 1; i <= 5; i++) {
  console.log(i);
}

// While loop
let count = 0;
while (count < 3) {
  console.log("Hello");
  count++;
}
